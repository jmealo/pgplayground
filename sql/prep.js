var weights = {
    relates_to: 1,
    dependency: 2
};

var rows = [];

function breakoutGroups(code) {
    var returnVal  = [],
        firstComma = code.indexOf(','),
        standard   = code.substr(0, firstComma - 1);

    code.substr(firstComma - 1).split(',').forEach(function (code) {
        getSubStandards(standard + code).forEach(function(code) {
            returnVal.push(code);
        });
    });

    return returnVal;
}

function lookupASN(code) {
    var asnId = altCodeToASN[code];
    return asnId ? asnId.asn_id : null;
}

Array.prototype.remove = function() {
    var what, a = arguments, L = a.length, ax;
    while (L && this.length) {
        what = a[--L];
        while ((ax = this.indexOf(what)) !== -1) {
            this.splice(ax, 1);
        }
    }
    return this;
};

links.forEach(function (link) {
    var targetCode   = link.target.name.split('||')[0],
        sourceCode   = link.source.name.split('||')[0],
        targetCodes  = [targetCode],
        sourceCodes  = [sourceCode],
        targetGroups = [],
        sourceGroups = [];

    if (!lookupASN(targetCode) && targetCode.indexOf(',') !== -1) {
        targetCodes = breakoutGroups(targetCode);
    }

    if (!lookupASN(sourceCode) && sourceCode.indexOf(',') !== -1) {
        sourceCodes = breakoutGroups(sourceCode);
    }

    targetGroups = targetCodes.filter(function (code) {
        return !lookupASN(code);
    });

    sourceGroups = sourceCodes.filter(function (code) {
        return !lookupASN(code);
    });

    targetGroups.forEach(function (code) {
        targetCodes = targetCodes.concat(getSubStandards(code));
        targetCodes.remove(code);
    });

    sourceGroups.forEach(function (code) {
        sourceCodes = sourceCodes.concat(getSubStandards(code));
        sourceCodes.remove(code);

        sourceCodes = sourceCodes.filter(function(elem, pos) {
            return sourceCodes.indexOf(elem) == pos;
        });
    });

    targetGroups.forEach(function (code) {
        targetCodes = targetCodes.concat(getSubStandards(code));
        targetCodes.remove(code);

        targetCodes = targetCodes.filter(function(elem, pos) {
            return targetCodes.indexOf(elem) == pos;
        });
    });

    sourceCodes.forEach(function(sourceCode) {
        var asn = lookupASN(sourceCode);

       targetCodes.forEach(function(targetCode) {
           rows.push({
               source: asn,
               target: lookupASN(targetCode),
               type:   link.type,
               weight: weights[link.type],
               link: link
           });
       });
    });
});

function getSubStandards(groupCode) {
    return Object.keys(altCodeToASN).filter(function (code) {
        return code.indexOf(groupCode) !== -1;
    });
}

function validateRows(rows) {
    var errors = 0;

    for (var x = 0, len = rows.length; x < len; x++) {

        if (rows[x].source == rows[x].target) {
            console.error('#' + (++errors) + ' cycle found at:\n' + JSON.stringify(rows[x], null, "\t"));
        }

        if (!rows[x].target) {
            console.error('#' + (++errors) + ' null target found at:\n' + JSON.stringify(rows[x], null, "\t"));
        }

        if (!rows[x].source) {
            console.error('#' + (++errors) + ' null source found at:\n' + JSON.stringify(rows[x], null, "\t"));
        }
    }

    return errors;
}


